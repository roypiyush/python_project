def create_np_array(data):
    array = np.array(data).astype(np.int)
    return array


def scatter_plot(axes, array):
    axes[0].set_title('Scatter')
    axes[0].plot(array, 'r.')


def histo(axes, array):
    axes[1].set_title('Histogram')
    axes[1].hist(array, color='#5ABCE6', edgecolor='black',
                 bins=np.arange(array.min(), array.max() + 1, 5000, np.int))


def create_graphs(data_1, idx):
    array = create_np_array(data_1)
    figure, axes = plt.subplots(2, 1)
    graph_title = "API Call Graphs" + "_" + str(idx + 1)
    figure.canvas.set_window_title(graph_title)
    figure.suptitle(graph_title)
    scatter_plot(axes, array)
    histo(axes, array)


